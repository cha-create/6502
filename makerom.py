

code = bytearray([
    0xa9, 0xff,
    0x8d, 0x02, 0x60,
    
    0xa2, 0x00,
    
    0x8e, 0x00, 0x60,
    0xe8,
    0x4c, 0x07, 0x80,

    ])

rom = code + bytearray([0xea] * (32768 - len(code)))



rom[0x7ffc] = 0x00
rom[0x7ffd] = 0x80



with open("rom.bin", "wb") as out_file:
    out_file.write(rom);


